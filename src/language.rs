impl Language {
    pub fn get_system_language() -> Language {
        let sys_lcid = unsafe { windows::Win32::Globalization::GetSystemDefaultLCID() };

        TABLE
            .iter()
            .find(|&&(lcid, ..)| lcid as u32 == sys_lcid)
            .map_or(Language::English_UnitedStates, |&(_, lang, ..)| lang)
    }
}

